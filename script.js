function expandDetails() {
  var btn = document.getElementsByClassName("content");
  var i = 0;
  for (i ; i < btn.length ; i++) {
    if (btn[i].style.display === 'none') {
      btn[i].style.display = 'flex';
    }
    else {
      btn[i].style.display = 'none';
    }
  }
}

